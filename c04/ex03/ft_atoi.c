int	ft_atoi(char *str)
{
	int	a;
	int	b;
	int	i;

	a = 1;
	b = 0;
	i = 0;
	while (((str[i] >= 9 && str[i] <= 13) || (str[i] == 32)) && (str[i]))
	{
		i++;
	}
	while ((str[i] == '+' || str[i] == '-') && (str[i]))
	{
		if (str[i] == '-')
			a = a * -1;
		i++;
	}
	while ((str[i] >= 48 && str[i] <= 57) && (str[i]))
	{
		b = b * 10;
		b = b + str[i] - 48;
		i++;
	}
	return (b * a);
}
