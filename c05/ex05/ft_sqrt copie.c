#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (0);
	while (a * a != nb)
	{
		a++;
		if (a > nb)
			return (0);
	}
	return (a);
}
