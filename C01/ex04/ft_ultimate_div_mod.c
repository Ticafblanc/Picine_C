#include <stdio.h>
void	ft_ultimate_div_mod(int *a, int *b)
{
	int	c;
	int	d;

	c = *b;
	*b = *a % *b;
	d = *b;
	*b = c;
	*a = *a / *b;
	*b = d;
}
